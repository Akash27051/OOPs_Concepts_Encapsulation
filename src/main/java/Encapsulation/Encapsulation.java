package Encapsulation;

public class Encapsulation {
    private int id=10;
    private String name="Kundan";
    private int age=25;
    private String gender="male";
    private int salary=80000;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getGender() {
        return gender;
    }

    public int getSalary() {
        return salary;
    }


}
